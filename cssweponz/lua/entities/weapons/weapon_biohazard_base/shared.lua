/*---------------------------------------------------------
	PrimaryAttack
---------------------------------------------------------*/
function SWEP:PrimaryAttack()
	local recoil = self.Primary.Recoil
	if self.Weapon:GetNetworkedBool( "Ironsights" ) then recoil = recoil * 0.5 end
	if self.Owner:KeyDown(IN_DUCK) then recoil = recoil * 0.5 end
	local cone = self.Primary.Cone
	cone = cone * math.Clamp(self.Owner:GetVelocity():Length() / 150, 1, 5)
	
	self.Weapon:SetNextSecondaryFire( CurTime() + self.Primary.Delay )
	self.Weapon:SetNextPrimaryFire( CurTime() + self.Primary.Delay )
	
	if ( !self:CanPrimaryAttack() ) then return end
	
	// Play shoot sound
	self.Weapon:EmitSound( self.Primary.Sound )
	
	// Shoot the bullet
	self:CSShootBullet( self.Primary.Damage, recoil, self.Primary.NumShots, cone )
	
	// Remove 1 bullet from our clip
	self:TakePrimaryAmmo( 1 )
	
	if ( self.Owner:IsNPC() ) then return end
	
	// Punch the player's view
	self.Owner:ViewPunch( Angle( math.Rand(-0.2,-0.1) * recoil, math.Rand(-0.1,0.1) * recoil, 0 ) )
	
	// In singleplayer this function doesn't get called on the client, so we use a networked float
	// to send the last shoot time. In multiplayer this is predicted clientside so we don't need to 
	// send the float.
	if ( (SinglePlayer() && SERVER) || CLIENT ) then
		self.Weapon:SetNetworkedFloat( "LastShootTime", CurTime() )
	end
	
end

local lbd = 0

function PenetrateCallback(n,att,tr,dmg)
	if tr.HitWorld and !tr.HitSky and n < 6 and att.lbd > 0 and tr.FractionLeftSolid < 0.01 then
		if CLIENT then return end
		local dst = tr.StartPos:Distance(tr.HitPos)
		local bullet = {}
		bullet.Num 		= 1
		bullet.Src 		= tr.HitPos+att:GetAimVector()*4
		bullet.Dir 		= att:GetAimVector()
		bullet.Spread 	= Vector( 0, 0, 0 )
		bullet.Tracer	= 2
		bullet.Force	= 5
		bullet.Damage	= math.floor(math.max(att.lbd*(1-tr.FractionLeftSolid*1000),0)/n)
		--print(tr.FractionLeftSolid)
		att.lbd = bullet.Damage
		bullet.Callback = function(a,b,c) PenetrateCallback(n+1,a,b,c) end
		timer.Simple(0,att.FireBullets,att,bullet)
	end
end

/*---------------------------------------------------------
   Name: SWEP:PrimaryAttack( )
   Desc: +attack1 has been pressed
---------------------------------------------------------*/
function SWEP:CSShootBullet( dmg, recoil, numbul, cone, tr )

	numbul 	= numbul 	or 1
	cone 	= cone 		or 0.01

	local bullet = {}
	bullet.Num 		= numbul
	bullet.Src 		= self.Owner:GetShootPos()			// Source
	bullet.Dir 		= self.Owner:GetAimVector()			// Dir of bullet
	bullet.Spread 	= Vector( cone, cone, 0 )			// Aim Cone
	bullet.Tracer	= tr or 2									// Show a tracer on every x bullets 
	bullet.Force	= 5									// Amount of force to give to phys objects
	bullet.Damage	= dmg
	self.Owner.lbd = dmg
	
	if bullet.Num == 1 then
		bullet.Callback = function(a,b,c) PenetrateCallback(1,a,b,c) end
	end
	
	self.Owner:FireBullets( bullet )
	self.Weapon:SendWeaponAnim( ACT_VM_PRIMARYATTACK ) 		// View model animation
	self.Owner:MuzzleFlash()								// Crappy muzzle light
	self.Owner:SetAnimation( PLAYER_ATTACK1 )				// 3rd Person Animation
	
	if ( self.Owner:IsNPC() ) then return end
	
	// CUSTOM RECOIL !
	if ( (SinglePlayer() && SERVER) || ( !SinglePlayer() && CLIENT && IsFirstTimePredicted() ) ) then
	
		local eyeang = self.Owner:EyeAngles()
		eyeang.pitch = eyeang.pitch - recoil
		self.Owner:SetEyeAngles( eyeang )
	
	end

end


